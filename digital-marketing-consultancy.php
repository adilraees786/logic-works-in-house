    document.addEventListener("DOMContentLoaded", function () {
        // Get all tabs and tab buttons
        const tabButtons = document.querySelectorAll(".unique-tab-button");
        const tabContents = document.querySelectorAll(".unique-tab-content");

        // Activate the first tab by default
        if (tabButtons.length > 0 && tabContents.length > 0) {
            const firstButton = tabButtons[0];
            const firstTab = tabContents[0];

            // Set the first tab to display
            firstTab.style.display = "block";

            // Add active class or any style to the first button (optional)
            firstButton.classList.add("active-tab");
        }

        // Tab switching logic
        tabButtons.forEach((button) => {
            button.addEventListener("click", (e) => {
                const targetTab = e.target.getAttribute("data-target");

                // Hide all tabs and stop their videos
                tabContents.forEach((tabContent) => {
                    tabContent.style.display = "none";
                    const video = tabContent.querySelector("video");
                    if (video) video.pause();
                });

                // Show the active tab
                const activeTab = document.getElementById(targetTab);
                activeTab.style.display = "block";

                // Update active tab button style (optional)
                tabButtons.forEach((btn) => btn.classList.remove("active-tab"));
                button.classList.add("active-tab");
            });
        });

        // Video play/pause logic
        document.querySelectorAll(".vidoe-div").forEach((videoDiv) => {
            const video = videoDiv.querySelector("video");
            const thumbnail = videoDiv.querySelector("img[alt='Video Thumbnail']");
            const controlsOverlay = videoDiv.querySelector("#controls-overlay");
            const playIcon = controlsOverlay.querySelector("#play-icon");
            const pauseIcon = controlsOverlay.querySelector("#pause-icon");

            const togglePlayPause = () => {
                if (video.paused) {
                    video.play();
                } else {
                    video.pause();
                }
            };

            const updateUI = () => {
                const isPlaying = !video.paused;
                thumbnail.style.display = isPlaying ? "none" : "block";
                playIcon.style.display = isPlaying ? "none" : "block";
                pauseIcon.style.display = isPlaying ? "block" : "none";
                controlsOverlay.style.display = isPlaying ? "none" : "block";
            };

            thumbnail.addEventListener("click", togglePlayPause);
            playIcon.addEventListener("click", togglePlayPause);
            pauseIcon.addEventListener("click", togglePlayPause);

            video.addEventListener("play", updateUI);
            video.addEventListener("pause", updateUI);
        });
    });
</script>